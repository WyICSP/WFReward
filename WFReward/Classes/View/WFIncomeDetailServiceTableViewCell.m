#import "WFIncomeDetailServiceTableViewCell.h"

@implementation WFIncomeDetailServiceTableViewCell

static NSString *const cellId = @"WFIncomeDetailServiceTableViewCell";

+ (instancetype)cellWithTableView:(UITableView *)tableView {
    WFIncomeDetailServiceTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellId];
    if (cell == nil) {
        cell = [[[NSBundle bundleForClass:[self class]] loadNibNamed:@"WFIncomeDetailServiceTableViewCell" owner:nil options:nil] firstObject];
    }
    return cell;
}

- (void)awakeFromNib {
    [super awakeFromNib];
    self.selectionStyle = 0;
}

- (void)setSelected:(BOOL)selected animated:(BOOL)animated {
    [super setSelected:selected animated:animated];

    // Configure the view for the selected state
}

@end
